package firstclasscode;

public class Switch {

	public static void main(String[] args) {
		int name=1;
		switch (name){
			
			case 1:
				System.out.println("chethan like mango");
			break;
			case 2:
				System.out.println("ram like apple");
			break;
			case 3:
				System.out.println("sham like grapes");
			break;
			case 4:
				System.out.println("anu like wine");
			default:System.out.println("invalid");
		}

	}

}
